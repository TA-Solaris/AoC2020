def getTrees(x, y, myInput):
    treeCount, temp_x, temp_y = 0, 0, 0
    for row in myInput:
        if temp_y == 0:
            if row[temp_x] == "#":
                treeCount += 1
            temp_x += x
            if temp_x >= len(row):
                temp_x += -len(row)
        temp_y += 1
        if temp_y == y:
            temp_y = 0
    return treeCount

def main(myInput):
    myInput = myInput.splitlines()
    print(getTrees(1, 1, myInput)*getTrees(3, 1, myInput)*getTrees(5, 1, myInput)*getTrees(7, 1, myInput)*getTrees(1, 2, myInput))

